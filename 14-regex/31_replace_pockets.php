<?php
	/*
	 * Карманы при замене через регулярки на PHP
	 */

	// ⊗ppPmRgRP
	
	/* ------------- №1 ------------- */
	$str = '12 34 56 78';
	$reg = '#(\d)(\d)#';
	$res = preg_replace($reg, '$2$1', $str);
	var_dump($res); // string(11) "21 43 65 87"
	
	/* ------------- №2 ------------- */
	$str = '31.12.2025'; // 2025.12.31
	$reg = '#(\d{2})\.(\d{2})\.(\d{4})#';
	$res = preg_replace($reg, '$3.$2.$1', $str);
	var_dump($res); // string(10) "2025.12.31"