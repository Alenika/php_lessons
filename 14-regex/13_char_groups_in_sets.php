<?php
	/*
	 * Группы символов внутри квадратных скобок PHP
	 */

	// ⊗ppPmRgSG
	
	$str = '123 .45 6.7 890... abc a#@ b1234c $%%^&@! d1234567e';
	
	/* ------------- №1 ------------- */
	$res = preg_replace('#[\d.]+#', '!', $str);
	echo $res . PHP_EOL; // ! ! ! ! abc a#@ b!c $%%^&@! d!e
	
	/* ------------- №2 ------------- */
	$res = preg_replace('#[^\da-g]{3,7}#', '!', $str);
	echo $res . PHP_EOL; // 123 .45 6.7 890!abc a!b1234c!! d1234567e