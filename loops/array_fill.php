<?php
	/*
	 * Заполнение массивов в PHP
	 */

	// ⊗ppPmLpALF
	
	/* ----------  №1  ---------- */

	$arr = [];
	
	for ($i = 1; $i <= 100; $i++) {
		$arr[] = $i;
	}
	
	var_dump($arr); // [99]=>int(100)
	
	/* ----------  №2  ---------- */

	$arr = [];
	
	for ($i = 1; $i <= 99; $i += 2) {
			$arr[] = $i;
	}
	
	var_dump($arr); // [48]=>int(97) [49]=>int(99)