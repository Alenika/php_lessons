<?php
	/*
	 * Формирование строк через циклы в PHP
	 */

	// ⊗ppPmLpSF

	/* ----------------- №1 ----------------- */

	$str = '';
	
	for ($i = 1; $i <= 5; $i++) {
		$str .= '-';
	}
	
	echo $str . ' '; // -----
	
	/* ----------------- №2 ----------------- */

	$str = '';
	
	for ($i = 1; $i <= 9; $i++) {
		$str .= $i;
	}

	echo $str . ' '; // 123456789
	
	/* ----------------- №3 ----------------- */

	$str = '';
	
	for ($i = 9; $i >= 1; $i--) {
		$str .= $i;
	}
	
	echo $str . ' '; // 987654321