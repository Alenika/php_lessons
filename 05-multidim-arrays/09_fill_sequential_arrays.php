<?php
	/*
	 * Заполнение многомерных массивов числами по порядку в PHP
	 */

	// ⊗ppPmMdNF
	
	/* ---------- №1 ---------- */

	$arr = [];
	
	for ($i = 0, $k = 1; $i < 4; $i++) {
		for ($j = 0; $j < 2; $j++) {
			$arr[$i][$j] = $k++;
		}
	}
	
	var_dump($arr);
	
	/* ---------- №2 ---------- */

	$arr = [];
	
	for ($i = 0, $k = 2; $i < 4; $i++) {
		for ($j = 0; $j < 3; $j++) {
			$arr[$i][$j] = $k;
			$k += 2;
		}
	}
	
	var_dump($arr);